func resourceInsightSelectorsCreate(ctx context.Context, d *schema.ResourceData, meta any) diag.Diagnostics {
	var diags diag.Diagnostics
	conn := meta.(*conns.AWSClient).CloudTrailClient(ctx)

	input := &cloudtrail.PutInsightSelectorsInput{
		InsightSelectors: expandInsightSelector(d.Get("insight_selector").(*schema.Set).List()),
	}

	if v, ok := d.GetOk("trail_name"); ok {
		input.TrailName = aws.String(v.(string))
	} else {
		input.EventDataStore = aws.String(d.Get("event_data_store").(string))
		input.InsightsDestination = aws.String(d.Get("insights_destination").(string))
	}

	output, err := conn.PutInsightSelectors(ctx, input)
	if err != nil {
		return sdkdiag.AppendErrorf(diags, "creating CloudTrail Insight Selectors: %s", err)
	}

	if input.TrailName != nil {
		d.SetId(aws.ToString(output.TrailARN))
	} else {
		d.SetId(aws.ToString(output.EventDataStoreArn))
	}

	return append(diags, resourceInsightSelectorsRead(ctx, d, meta)...)
}

func resourceInsightSelectorsRead(ctx context.Context, d *schema.ResourceData, meta any) diag.Diagnostics {
	var diags diag.Diagnostics
	conn := meta.(*conns.AWSClient).CloudTrailClient(ctx)

	id := d.Id()

	var (
		output *cloudtrail.GetInsightSelectorsOutput
		err    error
	)
	if isEventDataStoreID(id) {
		output, err = findInsightSelectorsByEventDataStore(ctx, conn, id)
	} else {
		output, err = findInsightSelectorsByTrailName(ctx, conn, id)
	}

	if !d.IsNewResource() && retry.NotFound(err) {
		log.Printf("[WARN] CloudTrail Insight Selectors (%s) not found, removing from state", id)
		d.SetId("")
		return diags
	}

	if err != nil {
		return sdkdiag.AppendErrorf(diags, "reading CloudTrail Insight Selectors (%s): %s", id, err)
	}

	if err := d.Set("insight_selector", flattenInsightSelector(output.InsightSelectors)); err != nil {
		return sdkdiag.AppendErrorf(diags, "setting insight_selector: %s", err)
	}

	if isEventDataStoreID(id) {
		d.Set("insights_destination", output.InsightsDestination)
	}

	return diags
}

func resourceInsightSelectorsUpdate(ctx context.Context, d *schema.ResourceData, meta any) diag.Diagnostics {
	var diags diag.Diagnostics
	conn := meta.(*conns.AWSClient).CloudTrailClient(ctx)

	id := d.Id()

	input := &cloudtrail.PutInsightSelectorsInput{
		InsightSelectors: expandInsightSelector(d.Get("insight_selector").(*schema.Set).List()),
	}
	if isEventDataStoreID(id) {
		input.EventDataStore = aws.String(id)
		input.InsightsDestination = aws.String(d.Get("insights_destination").(string))
	} else {
		input.TrailName = aws.String(id)
	}

	if _, err := conn.PutInsightSelectors(ctx, input); err != nil {
		return sdkdiag.AppendErrorf(diags, "updating CloudTrail Insight Selectors (%s): %s", id, err)
	}

	return append(diags, resourceInsightSelectorsRead(ctx, d, meta)...)
}

func resourceInsightSelectorsDelete(ctx context.Context, d *schema.ResourceData, meta any) diag.Diagnostics {
	var diags diag.Diagnostics
	conn := meta.(*conns.AWSClient).CloudTrailClient(ctx)

	id := d.Id()

	input := &cloudtrail.PutInsightSelectorsInput{
		InsightSelectors: []types.InsightSelector{},
	}
	if isEventDataStoreID(id) {
		input.EventDataStore = aws.String(id)
		if v, ok := d.GetOk("insights_destination"); ok {
			input.InsightsDestination = aws.String(v.(string))
		}
	} else {
		input.TrailName = aws.String(id)
	}

	_, err := conn.PutInsightSelectors(ctx, input)
	if err != nil {
		if errs.IsA[*types.TrailNotFoundException](err) ||
			errs.IsA[*types.InsightNotEnabledException](err) ||
			errs.IsA[*types.EventDataStoreNotFoundException](err) {
			return diags
		}
		return sdkdiag.AppendErrorf(diags, "deleting CloudTrail Insight Selectors (%s): %s", id, err)
	}

	return diags
}

func findInsightSelectorsByTrailName(ctx context.Context, conn *cloudtrail.Client, trailName string) (*cloudtrail.GetInsightSelectorsOutput, error) {
	input := &cloudtrail.GetInsightSelectorsInput{
		TrailName: aws.String(trailName),
	}

	output, err := conn.GetInsightSelectors(ctx, input)
	if errs.IsA[*types.TrailNotFoundException](err) ||
		errs.IsA[*types.InsightNotEnabledException](err) {
		return nil, &retry.NotFoundError{
			LastError: err,
		}
	}
	if err != nil {
		return nil, err
	}

	return output, nil
}

func findInsightSelectorsByEventDataStore(ctx context.Context, conn *cloudtrail.Client, edsARN string) (*cloudtrail.GetInsightSelectorsOutput, error) {
	input := &cloudtrail.GetInsightSelectorsInput{
		EventDataStore: aws.String(edsARN),
	}

	output, err := conn.GetInsightSelectors(ctx, input)
	if errs.IsA[*types.EventDataStoreNotFoundException](err) ||
		errs.IsA[*types.EventDataStoreARNInvalidException](err) ||
		errs.IsA[*types.InsightNotEnabledException](err) {
		return nil, &retry.NotFoundError{
			LastError: err,
		}
	}
	if err != nil {
		return nil, err
	}

	return output, nil
}

// isEventDataStoreID reports whether id is an event data store ARN.
// Trail ARNs have the form arn:...:trail/<name>; EDS ARNs have the form arn:...:eventdatastore/<id>.
func isEventDataStoreID(id string) bool {
	parsed, err := arn.Parse(id)
	if err != nil {
		return false
	}
	return strings.HasPrefix(parsed.Resource, "eventdatastore/")
}

// insightSelectorsImportStatePassthrough sets trail_name or event_data_store from the
// imported ID (which is always an ARN) so that the subsequent Read can determine the mode.
func insightSelectorsImportStatePassthrough(_ context.Context, d *schema.ResourceData, _ any) ([]*schema.ResourceData, error) {
	if isEventDataStoreID(d.Id()) {
		d.Set("event_data_store", d.Id())
	} else {
		d.Set("trail_name", d.Id())
	}
	return []*schema.ResourceData{d}, nil
}
